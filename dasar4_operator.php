<?php
$a = 5;
$b = 2;

// += Penjumlahan
$c = $a + $b;
echo "$a + $b = $c"; 
echo "<hr>";

// - = Pengurangan
$c = $a - $b; 
echo "$a - $b = $c"; 
echo "<hr>";

// * = Perkalian
$c = $a * $b; 
echo "$a * $b = $c";
echo "<hr>";

// ** = Pemangkatan
$c = $a ** $b;
echo "$a ** $b = $c";
echo "<hr>";

// / = Pembagian
$c = $a / $b;
echo "$a / $b = $c";
echo "<hr>";

// % = Sisa Bagi/Modulo
$c = $a % $b;
echo "$a % $b = $c";
echo "<hr>";
?>